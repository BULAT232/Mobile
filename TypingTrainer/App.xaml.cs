namespace TypingTrainer;

public partial class App : Application
{
    public App()
    {
        // Загружаем XAML-ресурсы (стили, цвета, конвертеры из App.xaml)
        InitializeComponent();

        // Устанавливаем AppShell как главную страницу —
        // это определяет всю навигационную структуру приложения
        MainPage = new AppShell();
    }
}
